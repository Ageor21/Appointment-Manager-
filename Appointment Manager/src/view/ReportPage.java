    private int countAptsByCounselor(Counselor counselor) {
        // USED: lambda
        return (int) appointments
                .stream()
                .filter(c -> c.getCounselor().equals(counselor))
                .count();
    }

    private int countAptsByAptType(APTType type) {
        // USED: lambda
        return (int) appointments
                .stream()
                .filter(c -> c.getType().equals(type))
                .count();
    }

    private int countAptsByState(String state) {
        // USED: lambda
        return (int) appointments
                .stream()
                .filter(c -> c.getPatient().getAddress().getState().equalsIgnoreCase(state))
                .count();
    }

