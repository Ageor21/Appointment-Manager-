    private void cbxCounselorsItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxCounselorsItemStateChanged

        Counselor counselor = (Counselor) cbxCounselors.getSelectedItem();
        if (counselor == null) {
            txtNoOfAptByCounselors.setText("0");
        } else {
            int count = countAptsByCounselor(counselor);
            txtNoOfAptByCounselors.setText(count + "");
        }
    }//GEN-LAST:event_cbxCounselorsItemStateChanged

    private void cbxAptTypesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxAptTypesItemStateChanged
        APTType type = (APTType) cbxAptTypes.getSelectedItem();
        if (type == null) {
            txtNoOfAptByType.setText("0");
        } else {
            int count = countAptsByAptType(type);
            txtNoOfAptByType.setText(count + "");
        }
    }//GEN-LAST:event_cbxAptTypesItemStateChanged

    private void txtStateKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtStateKeyReleased
        String state = txtState.getText().trim();
        if (state.isEmpty()) {
            txtNoOfAptByState.setText("0");
        } else {
            int count = countAptsByState(state);
            txtNoOfAptByState.setText(count + "");
        }
    }//GEN-LAST:event_txtStateKeyReleased


