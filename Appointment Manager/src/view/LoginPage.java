    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText().trim();
        String password = txtPassword.getText().trim();
        String pin = txtPin.getText().trim();

        if (username.isEmpty() || password.isEmpty() || pin.isEmpty()) {
            String erroMsg = prop.getProperty(desktopLanguage + ".empty_cred");
            JOptionPane.showMessageDialog(this, erroMsg);
            helper.Logger.errorLogin(erroMsg, username, password, pin);
        } else {
            Counselor user = null;
            for (Counselor cr : counselors) {
                if (cr.isAuthenticated(username, password, pin)) {
                    user = cr;
                    break;
                }
            }
            if (user == null) {
                // login failed
                String erroMsg = prop.getProperty(desktopLanguage + ".wrong_cred");
                JOptionPane.showMessageDialog(this, erroMsg);
                helper.Logger.errorLogin(erroMsg, username, password, pin);
            } else {
                // login success
                this.setVisible(false);
                HomePage.getInstance().login(user);
                helper.Logger.successLogin(user);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

